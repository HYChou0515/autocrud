/**
 * Tests for codegen/routes-gen.ts — Route page generation.
 */
import { describe, it, expect } from 'vitest';
import { buildIR } from '../test-helpers.js';
import {
  genMigratePage,
  genCreateRoute,
  genBackupPage,
  genListRoute,
  genDetailRoute,
  genRootIndex,
  genDashboard,
} from './routes-gen.js';

// ─── Helpers ────────────────────────────────────────────────────────────────

function parse(spec: any, basePath = '') {
  return buildIR(spec, basePath).resources;
}

function buildSimpleSpec(basePath = '') {
  const prefix = basePath || '';
  return {
    info: { title: 'Test', version: '1.0' },
    paths: {
      [`${prefix}/character`]: {
        post: {
          requestBody: { content: { 'application/json': { schema: { $ref: '#/components/schemas/Character' } } } },
        },
      },
      [`${prefix}/skill`]: {
        post: { requestBody: { content: { 'application/json': { schema: { $ref: '#/components/schemas/Skill' } } } } },
      },
    },
    components: {
      schemas: {
        Character: {
          type: 'object',
          properties: { name: { type: 'string' }, level: { type: 'integer' } },
          required: ['name', 'level'],
        },
        Skill: { type: 'object', properties: { name: { type: 'string' } }, required: ['name'] },
      },
    },
  };
}

// ============================================================================
// genMigratePage
// ============================================================================
describe('genMigratePage', () => {
  it('generates a route page importing MigrationStatus component', () => {
    const resources = parse(buildSimpleSpec());
    const code = genMigratePage(resources);

    expect(code).toContain("import { createFileRoute } from '@tanstack/react-router'");
    expect(code).toContain("import { MigrationStatus } from '../../autocrud/lib/components/MigrationStatus'");
    expect(code).toContain("createFileRoute('/autocrud-admin/migrate')");
    expect(code).toContain('MigrationStatus');
  });

  it('passes all resource names to MigrationStatus', () => {
    const resources = parse(buildSimpleSpec());
    const code = genMigratePage(resources);

    expect(code).toContain("'character'");
    expect(code).toContain("'skill'");
    expect(code).toContain('resourceNames={[');
  });

  it('includes auto-generated comment header', () => {
    const resources = parse(buildSimpleSpec());
    const code = genMigratePage(resources);

    expect(code).toContain('// Auto-generated by AutoCRUD Web Generator');
  });
});

